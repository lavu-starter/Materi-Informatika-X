# Latihan List & Tuple
nilai_siswa = [70, 85, 90, 75]
print("Daftar nilai:", nilai_siswa)
nilai_siswa[2] = 95
print("Nilai diperbarui:", nilai_siswa)
nilai_siswa.append(80)
print("Ditambahkan:", nilai_siswa)
print("Nilai maksimum:", max(nilai_siswa))
print("Nilai minimum:", min(nilai_siswa))
tup_siswa = (1,2,3)
print("Tuple:", tup_siswa)
